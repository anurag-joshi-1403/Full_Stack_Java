public class TernaryOperator {
    public static void main(String[] args) {
        int a =13;
        String result = (a > 0) ? "Positive" : "Negative";
        System.out.println(result);

        int b = 10;
        String result1 = (a > b) ? "a" : "b";
        System.out.println(result1);
        
    }
    
}
